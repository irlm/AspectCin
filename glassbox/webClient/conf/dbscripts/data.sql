CREATE TABLE MAILSERVER (
    id INTEGER GENERATED BY DEFAULT AS IDENTITY(START WITH 1000) PRIMARY KEY,
	name VARCHAR(64),
    smtp VARCHAR(64),
    userName VARCHAR(64),
    password VARCHAR(32),
    UNIQUE(userName)
);